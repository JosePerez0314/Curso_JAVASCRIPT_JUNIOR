// Las operaciones

// Operadores de asignación y operadores aritmeticos.

/* Hay diferentes tipos de operadores de asignacion en los que podemos encontrar estan: */

// Asignación

x = y; 

// Asignación

x = y;

//Asignación de adicción 

x += y; 

//Asignación de adicción 

x = x + y;

//Asignación de sustracción

x -= y; 

//Asignación de sustracción

x = x - y;

//Asignación de multiplicación

x *= y; 

//Asignación de multiplicación

x = x * y;

//Asignación de división 

x /= y; 

//Asignación de división 

x = x /y;

// Asignación de resto

x %= y; 

// Asignación de resto

x = x % y;

//Asignación de potenciación 

x **= y; 

//Asignación de potenciación 

x = x ** y;

//Asignación de desplazamiento a la izquierda

x <<= y; 

//Asignación de desplazamiento a la izquierda

x = x << y;

//Asignación de desplazamiento a la derecha

x >>= y; 

//Asignación de desplazamiento a la derecha

x = x >> y;

//Asignación sin signo de desplazamiento a la derecha 

x >>>= y; 

//Asignación sin signo de desplazamiento a la derecha 

x = x >>> y;

//Asignación AND

x &= y; 

//Asignación AND

x = x & y;

//Aignación XOR

x ^= y; 

//Aignación XOR

x = x ^ y;

//Asignación OR 

x |= y; 

//Asignación OR 

x = x | y;